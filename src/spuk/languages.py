# A few names that .title() would mangle.
_DISPLAY_OVERRIDES = {
    "ht": "Haitian Creole",
}


def is_supported(code: str) -> bool:
    return code in LANGUAGES


def display_name(code: str) -> str:
    """Human-friendly name for a code (e.g. 'en' -> 'English'); echoes unknown codes."""
    if code in _DISPLAY_OVERRIDES:
        return _DISPLAY_OVERRIDES[code]
    name = LANGUAGES.get(code)
    return name.title() if name else code.upper()


def all_languages() -> list[tuple[str, str]]:
    """All (code, display name) pairs, sorted by display name — for the picker."""
    return sorted(((c, display_name(c)) for c in LANGUAGES), key=lambda x: x[1])
